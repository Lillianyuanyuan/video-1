let canvas = document.querySelector('canvas');
let con = canvas.getContext('2d');
[canvas.width, canvas.height] = [window.innerWidth, window.innerHeight];
con.fillStyle = 'red';
con.rect(20, 20, 50, 50);
con.fill();
con.closePath();
con.beginPath();
con.strokeStyle = 'black';
con.lineWidth = 10;
con.moveTo(60, 60);
con.lineTo(120, 120);
con.stroke();
con.closePath();
con.beginPath();
con.arc(160, 160, 40, 0, Math.PI / 2, true);
con.stroke();
con.closePath();
con.save();
con.translate(160, 160);
con.rotate(Math.PI / 2);
con.restore();